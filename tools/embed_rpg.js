const fs = require('fs')
const path = require('path')
const root = path.resolve(__dirname, '..')
const axn = fs.readFileSync(path.join(root, 'examples', 'rpg.axn'), 'utf8')
const js  = fs.readFileSync(path.join(root, 'demo', 'rpg.axon.js'), 'utf8')
const escape = s => s.replace(/\\/g, '\\\\').replace(/`/g, '\\`').replace(/\$/g, '\\$')
const out = `// Auto-generated by tools/embed_rpg.js
window.RPG_SOURCES = {
  axn: \`${escape(axn)}\`,
  js:  \`${escape(js)}\`,
};
`
fs.writeFileSync(path.join(root, 'demo', 'rpg.sources.js'), out, 'utf8')
console.log('✓ Wrote demo/rpg.sources.js')
